function shuffle(array) {
  var copy = [], n = array.length, i;

  // While there remain elements to shuffleâ€¦
  while (n) {

    // Pick a remaining elementâ€¦
    i = Math.floor(Math.random() * array.length);

    // If not already shuffled, move it to the new array.
    if (i in array) {
      copy.push(array[i]);
      delete array[i];
      n--;
    }
  }

  return copy;
}

